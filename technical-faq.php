                    <h3>1. How do I choose between SS 304 and SS 316 for my project?</h3>
                    <div class="faq-answer">
                        <p><strong>SS 304:</strong> This is the standard "18/8" stainless steel and is excellent for
                            most indoor applications or areas with moderate weather.</p>
                        <p><strong>SS 316:</strong> Contains molybdenum, which provides superior resistance to chlorides
                            and marine environments. It is the highly recommended choice for coastal regions in India or
                            industrial zones with high chemical exposure.</p>
                    </div>
                </div>

                <div class="faq-item">
                    <h3>2. What is the difference between Ferrule Type and Hand-Woven Cable Mesh?</h3>
                    <div class="faq-answer">
                        <p><strong>Ferrule Type:</strong> The cables are held together by high-strength seamless
                            ferrules. It offers a modern, rigid aesthetic and is widely used for safety balustrades.</p>
                        <p><strong>Hand-Woven:</strong> The cables are physically interwoven. This provides higher
                            flexibility and "stretch," making it ideal for the irregular shapes of zoo enclosures and
                            aviaries.</p>
                    </div>
                </div>

                <div class="faq-item">
                    <h3>3. Does G.I. (Galvanized Iron) mesh eventually rust?</h3>
                    <div class="faq-answer">
                        <p>Our G.I. products are electro-galvanized to provide a protective zinc barrier that resists
                            oxidation.</p>
                        <p>While significantly more rust-resistant than plain carbon steel, its lifespan depends on
                            environmental factors. For maximum longevity in high-moisture areas, we recommend our
                            Stainless Steel range.</p>
                    </div>
                </div>

