            // Add event listener for opening and closing details
            $('#specTable tbody').on('click', 'td.details-control', function () {
                var tr = $(this).closest('tr');
                var row = table.row(tr);
                var breakingLoad = tr.data('breaking-load');
                var icon = $(this).find('i');

                if (row.child.isShown()) {
                    // This row is already open - close it
                    row.child.hide();
                    tr.removeClass('shown');
                    icon.removeClass('fa-minus-circle').addClass('fa-plus-circle');
                }
                else {
                    // Open this row
                    row.child(format(breakingLoad)).show();
                    tr.addClass('shown');
                    icon.removeClass('fa-plus-circle').addClass('fa-minus-circle');
                }
            });

            function format(d) {
                // `d` is the breaking load value
                return '<div class="child-row-content">' +
                    '<strong>Min. Breaking Load:</strong> ' + d + ' (KN)' +
                    '</div>';
            }
        });
